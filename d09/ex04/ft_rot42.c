char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = (str[i] - 22) % 26 + 64;
		else if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = (str[i] - 54) % 26 + 96;
		i++;
	}
	return (str);
}
