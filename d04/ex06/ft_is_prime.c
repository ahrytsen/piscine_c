int		ft_is_prime(int nb)
{
	int i;
	int z;

	if ((nb % 2) && (nb > 2))
	{
		i = 3;
		z = nb / 2;
		while (i < z)
		{
			if (nb % i == 0)
				return (0);
			i += 2;
		}
		return (1);
	}
	else
		return (nb == 2);
}
